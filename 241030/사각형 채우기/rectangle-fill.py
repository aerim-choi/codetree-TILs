# n=5
# 8개
# 막대기가 5개 막대기가 3개 막대기가 2개
# |||||  =||| |=|| ||=| |||=  ==| |== =|= 

n = int(input())

dp = [-1 for _ in range(n+1)]
dp[1] = 1
dp[2] = 2

for i in range(3,n+1):
    dp[i] = dp[i-2]+dp[i-1]

print(dp[i])